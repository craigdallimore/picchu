//// IMPORTS //////////////////////////////////////////////////////////////////

const { Flux } = require('delorean');
var   store    = require('../store/store');

//// DISPATCHER ///////////////////////////////////////////////////////////////

let Dispatcher = Flux.createDispatcher({

  onNewImage(payload) {

    this.dispatch('onNewImage', payload);

  },

  getStores() {

    return {
      store : store
    };

  }

});

///// EXPORTS /////////////////////////////////////////////////////////////////

module.exports = Dispatcher;

