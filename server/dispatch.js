var tiles = require('./store').tiles;
var shoe  = require('shoe');

//// EXPORTS //////////////////////////////////////////////////////////////////

module.exports = function(server) {

  // https://github.com/substack/shoe
  // Shoe is given a function that is passed a stream as an argument.
  // Stuff can be written to the stream, and the output can be piped.
  shoe(tiles).install(server, '/link');

};

